public class Primitive {
    public static void main(String[] args) {
        int var = 1234567890;
        long var2 = 12345678901L;
        char ch = 'A';
        float var3 = 3.14F;
        double var4 = 0.12345;
        boolean var5 = true;
        System.out.println("var = " + var);
        System.out.println("var2 = " + var2);
        System.out.println("ch = " + var);
        System.out.println("var5 = " + var5);
        
    }
    
}
